package cz.vse.adventura.logika;

/**
 *  Výčtový typ Den - představuje dny v týdnu.
 *
 *  Tento výčtový typ zahrnuje všechny dny v týdnu, od pondělí do pátku
 *  Využívá se k pozdravu podle aktuálního dne.
 *
 *  @author Jan Kornienko
 *  @version pro LS 2023/2024
 */
public enum Den {
    MONDAY,
    TUESDAY,
    WEDNESDAY,
    THURSDAY,
    FRIDAY,
    SATURDAY,
    SUNDAY
}
